// ─────────────────────────────────────────────────────────────────────────────
// Main Generator Function
// ─────────────────────────────────────────────────────────────────────────────

export function generateRuntimePlugin(
  config: HooksConfig,
  srcDir: string,
  distDir: string,
  platform: RuntimePlatform,
  version?: string,
): void {
  const pluginDir = join(distDir, "plugins");
  mkdirSync(pluginDir, { recursive: true });

  const code = generateCode(config, platform, version);
  const outputFile = platform.platform === "opencode" ? "hooks.ts" : "loaf.js";
  writeFileSync(join(pluginDir, outputFile), code);
}

// ─────────────────────────────────────────────────────────────────────────────
// Code Generation
// ─────────────────────────────────────────────────────────────────────────────

function generateCode(
  config: HooksConfig,
  platform: RuntimePlatform,
  version?: string,
): string {
  const header = platform.header.replace("{{VERSION}}", version || "unknown");

  const coreFunctions = generateCoreFunctions(platform);
  const hookData = generateHookData(config);
  const pluginBody = generatePluginBody(config, platform);

  return `${header}

import { execFile } from 'child_process';
import { promisify } from 'util';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const execFileAsync = promisify(execFile);

${coreFunctions}

${hookData}

${platform.wrapExport(pluginBody)}`;
}

function generateCoreFunctions(platform: RuntimePlatform): string {
  return `// ─────────────────────────────────────────────────────────────────────────────
// Core Hook Functions
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Run a hook via subprocess (loaf check or script execution)
 * 
 * Exit codes:
 *   0 = allow (hook passed)
 *   1 = error (hook failed but not blocking)
 *   2 = block (hook rejected the action)
 * 
 * @param hookType - 'pre-tool', 'post-tool', or 'session'
 * @param toolName - Name of the tool being invoked
 * @param hookId - Specific hook identifier
 * @param command - Direct command to execute (alternative to script)
 * @param script - Script file to execute
 * @param timeout - Maximum execution time in milliseconds
 * @param failClosed - When true, subprocess errors block the action
 * @returns Hook result with exit code, output, and error information
 */
interface HookResult {
  exitCode: number;
  stdout: string;
  stderr: string;
  error?: string;
}

function serializeHookPayload(toolName: string, toolInput: unknown, rawInput?: unknown): string | undefined {
  const normalizedToolInput =
    toolInput && typeof toolInput === 'object'
      ? toolInput
      : toolInput === undefined
        ? {}
        : { value: toolInput };

  try {
    return JSON.stringify({
      tool: { name: toolName || '' },
      tool_name: toolName || '',
      tool_input: normalizedToolInput,
      input: normalizedToolInput,
      raw: rawInput,
    });
  } catch {
    return undefined;
  }
}

async function runHook(
  hookType: string,
  toolName: string,
  hookId: string,
  command?: string,
  script?: string,
  payload?: string,
  timeout: number = 60000,
  failClosed: boolean = false,
): Promise<HookResult> {
  const env = {
    ...process.env,
    LOAF_HOOK_TYPE: hookType,
    LOAF_HOOK_ID: hookId,
    LOAF_TOOL_NAME: toolName || '',
  };

  try {
    // If hook has direct command (e.g., 'loaf check ...'), execute it
    if (command) {
      const child = execFile('bash', ['-c', command], {
        cwd: process.cwd(),
        env,
        encoding: 'utf-8',
        timeout,
      });
      if (payload && child.stdin) {
        child.stdin.write(payload);
        child.stdin.end();
      }
      
      const result = await new Promise<HookResult>((resolve) => {
        let stdoutStr = '';
        let stderrStr = '';
        child.stdout?.on('data', (data) => stdoutStr += data);
        child.stderr?.on('data', (data) => stderrStr += data);
        child.on('close', (code) => {
          resolve({ exitCode: code || 0, stdout: stdoutStr, stderr: stderrStr });
        });
        child.on('error', (err) => {
          resolve({ exitCode: failClosed ? 2 : 1, stdout: stdoutStr, stderr: stderrStr || err.message, error: err.message });
        });
      });
      return result;
    }

    // Otherwise run the script file
    if (script) {
      const scriptPath = join(__dirname, 'hooks', script);
      const interpreter = script.endsWith('.py') ? 'python3' : 'bash';
      const child = execFile(interpreter, [scriptPath], {
        cwd: process.cwd(),
        env,
        encoding: 'utf-8',
        timeout,
      });
      if (payload && child.stdin) {
        child.stdin.write(payload);
        child.stdin.end();
      }
      
      const result = await new Promise<HookResult>((resolve) => {
        let stdoutStr = '';
        let stderrStr = '';
        child.stdout?.on('data', (data) => stdoutStr += data);
        child.stderr?.on('data', (data) => stderrStr += data);
        child.on('close', (code) => {
          resolve({ exitCode: code || 0, stdout: stdoutStr, stderr: stderrStr });
        });
        child.on('error', (err) => {
          resolve({ exitCode: failClosed ? 2 : 1, stdout: stdoutStr, stderr: stderrStr || err.message, error: err.message });
        });
      });
      return result;
    }

    return { exitCode: 1, stdout: '', stderr: 'No command or script specified' };
  } catch (error: any) {
    return {
      exitCode: failClosed ? 2 : 1,
      stdout: '',
      stderr: error.message,
      error: error.message,
    };
  }
}

/**
 * Check if a tool name matches a pattern
 * Patterns can be:
 *   - Simple wildcard: "Bash*" matches "Bash", "BashFile", etc.
 *   - Union pattern: "Edit|Write" matches either
 *   - Exact match: "Edit" matches only "Edit"
 */
function matchesTool(toolName: string, pattern: string): boolean {
  if (!toolName || !pattern) return false;
  
  const patterns = pattern.split('|');
  return patterns.some((p) => {
    const trimmed = p.trim();
    if (trimmed.endsWith('*')) {
      return toolName.startsWith(trimmed.slice(0, -1));
    }
    return toolName === trimmed;
  });
}

function matchesIfCondition(toolName: string, toolInput: unknown, ifCondition: string | undefined): boolean {
  if (!ifCondition) return true;
  
  // Parse pattern like "Bash(gh pr merge:*)" or "Bash(git push:*)"
  // Tool name before '(' and command pattern inside parentheses
  const match = ifCondition.match(/^(\\w+)\\(([^)]+)\\)$/);
  if (!match) return true;
  
  const [, expectedTool, commandPattern] = match;
  if (toolName !== expectedTool) return false;
  
  const input = toolInput as Record<string, unknown> | undefined;
  const command = (input?.command || input?.file_path) as string | undefined;
  if (!command) return false;
  
  // Handle glob patterns with :* suffix (e.g., "git commit:*" means "starts with git commit")
  // The :* format treats everything before : as a literal prefix, * matches anything after
  if (commandPattern.endsWith(':')) {
    // Pattern like "git commit:" - match commands starting with this prefix
    const prefix = commandPattern.slice(0, -1);
    return command.startsWith(prefix);
  }
  
  if (commandPattern.endsWith(':*')) {
    // Pattern like "git commit:*" - match commands starting with "git commit"
    const prefix = commandPattern.slice(0, -2);
    return command.startsWith(prefix);
  }
  
  // For other patterns, convert glob to regex
  // Escape regex special characters then convert * to .* and ? to .
  let regexPattern = commandPattern
    .replace(/[.+^\$"{}()|[\]\\]/g, '\\$&')
    .replace(/\*/g, '.*')
    .replace(/\?/g, '.');
  
  const regex = new RegExp('^' + regexPattern + '$');
  return regex.test(command);
}
`;
}

function generateHookData(config: HooksConfig): string {
  const preToolHooks = config.hooks["pre-tool"] || [];
  const postToolHooks = config.hooks["post-tool"] || [];
  const sessionHooks = config.hooks.session || [];

  // Filter out prompt hooks — runtime plugins only support command/script hooks
  const commandPreToolHooks = preToolHooks.filter((h) => h.type !== "prompt");
  const commandPostToolHooks = postToolHooks.filter((h) => h.type !== "prompt");
  const commandSessionHooks = sessionHooks.filter((h) => h.type !== "prompt");

  // Group pre-tool hooks by matcher
  const preToolByMatcher = groupHooksByMatcher(commandPreToolHooks);
  const postToolByMatcher = groupHooksByMatcher(commandPostToolHooks);

  // Group session hooks by event
  const sessionByEvent = groupSessionHooksByEvent(commandSessionHooks);

  return `// ─────────────────────────────────────────────────────────────────────────────
// Hook Data
// ─────────────────────────────────────────────────────────────────────────────

interface HookEntry {
  id: string;
  command?: string;
  script?: string;
  timeout: number;
  failClosed: boolean;
  if?: string;
}

const preToolHooks: Record<string, HookEntry[]> = ${JSON.stringify(preToolByMatcher, null, 2)};

const postToolHooks: Record<string, HookEntry[]> = ${JSON.stringify(postToolByMatcher, null, 2)};

const sessionHooks: Record<string, HookEntry[]> = ${JSON.stringify(sessionByEvent, null, 2)};`;
}

function generatePluginBody(config: HooksConfig, platform: RuntimePlatform): string {
  const preToolEvent = platform.events.preTool;
  const postToolEvent = platform.events.postTool;
  const sessionStartEvent = platform.events.sessionStart;
  const sessionEndEvent = platform.events.sessionEnd;

  let body = `    // Pre-tool hook handler
    '${preToolEvent}': async (input) => {
      const toolName = ${platform.toolNameAccessor};
      const toolInput = ${platform.toolInputAccessor};
      if (!toolName) return;

      const hookPayload = serializeHookPayload(toolName, toolInput, input);

      for (const [matcher, hookList] of Object.entries(preToolHooks)) {
        if (matchesTool(toolName, matcher)) {
          for (const hook of hookList) {
            if (!matchesIfCondition(toolName, toolInput, hook.if)) continue;
            const result = await runHook('pre-tool', toolName, hook.id, hook.command, hook.script, hookPayload, hook.timeout, hook.failClosed);
            
            // Exit code 2 = block the action
            if (result.exitCode === 2) {
              ${platform.rejectPattern('result.stderr')}
            }
            
            // Log errors for debugging
            if (result.exitCode === 1) {
              console.warn(\`[loaf] Hook \${hook.id} error: \${result.stderr}\`);
            }
          }
        }
      }
    },

    // Post-tool hook handler
    '${postToolEvent}': async (input) => {
      const toolName = ${platform.toolNameAccessor};
      const toolInput = ${platform.toolInputAccessor};
      if (!toolName) return;

      const hookPayload = serializeHookPayload(toolName, toolInput, input);

      for (const [matcher, hookList] of Object.entries(postToolHooks)) {
        if (matchesTool(toolName, matcher)) {
          for (const hook of hookList) {
            if (!matchesIfCondition(toolName, toolInput, hook.if)) continue;
            const result = await runHook('post-tool', toolName, hook.id, hook.command, hook.script, hookPayload, hook.timeout, hook.failClosed);
            
            if (result.exitCode !== 0) {
              console.warn(\`[loaf] Post-hook \${hook.id} error (exit \${result.exitCode}): \${result.stderr}\`);
            }
          }
        }
      }
    }`;

  // Add session lifecycle handlers if events are defined
  if (sessionStartEvent || sessionEndEvent) {
    body += `,\n`;
    
    if (platform.platform === "opencode") {
    // OpenCode uses event-based session handling
      body += generateOpenCodeSessionHandlers(config, platform);
    } else if (platform.platform === "amp") {
      // Amp uses direct handlers if available
      body += generateAmpSessionHandlers(config, platform);
    }
  }

  return body;
}

function generateOpenCodeSessionHandlers(config: HooksConfig, platform: RuntimePlatform): string {
  let body = `    // Session lifecycle event handler
    'event': async ({ event }) => {
      if (event.type === '${platform.events.sessionStart}' && sessionHooks.sessionstart) {
        for (const hook of sessionHooks.sessionstart) {
          await runHook('session', 'session', hook.id, hook.command, hook.script, undefined, hook.timeout, hook.failClosed);
        }
      }
      if (event.type === '${platform.events.sessionEnd}' && sessionHooks.sessionend) {
        for (const hook of sessionHooks.sessionend) {
          await runHook('session', 'session', hook.id, hook.command, hook.script, undefined, hook.timeout, hook.failClosed);
        }
      }`;

  // Add extra events (like context.compacting) inside the event handler
  const extraEvents = generateOpenCodeExtraEvents(config);
  if (extraEvents) {
    body += `\n${extraEvents}`;
  }

  body += `\n    }`;
  return body;
}

function generateOpenCodeExtraEvents(config: HooksConfig): string {
  // Check for session.compacting handlers (pre-compact-archive)
  const compactingHooks = config.hooks.session?.filter(
    (h) => h.event?.toLowerCase() === "precompact" && h.type !== "prompt"
  ) || [];

  if (compactingHooks.length === 0) return "";

  return `      if (event.type === 'context.compacting' && sessionHooks.precompact) {
        for (const hook of sessionHooks.precompact) {
          await runHook('session', 'session', hook.id, hook.command, hook.script, undefined, hook.timeout, hook.failClosed);
        }
      }`;
}

function generateAmpSessionHandlers(config: HooksConfig, platform: RuntimePlatform): string {
  let handlers = "";

  if (platform.events.sessionStart) {
    handlers += `  // Session start handler
  '${platform.events.sessionStart}': async () => {
    if (sessionHooks.sessionstart) {
      for (const hook of sessionHooks.sessionstart) {
        await runHook('session', 'session', hook.id, hook.command, hook.script, undefined, hook.timeout, hook.failClosed);
      }
    }
  },\n`;
  }

  if (platform.events.sessionEnd) {
    handlers += `  // Session end handler
  '${platform.events.sessionEnd}': async () => {
    if (sessionHooks.sessionend) {
      for (const hook of sessionHooks.sessionend) {
        await runHook('session', 'session', hook.id, hook.command, hook.script, undefined, hook.timeout, hook.failClosed);
      }
    }
  },\n`;
  }

  return handlers.slice(0, -2); // Remove trailing comma and newline
}

// ─────────────────────────────────────────────────────────────────────────────
// Helper Functions
// ─────────────────────────────────────────────────────────────────────────────

function groupHooksByMatcher(hooks: HookDefinition[]): Record<string, HookEntry[]> {
  const grouped: Record<string, HookEntry[]> = {};

  for (const hook of hooks) {
    const matcher = hook.matcher || "Edit|Write";
    if (!grouped[matcher]) grouped[matcher] = [];

    // Synthesize command for enforcement hooks that don't have explicit command/script
    let command = hook.command;
    if (!command && !hook.script && ENFORCEMENT_HOOKS.has(hook.id)) {
      command = `loaf check --hook ${hook.id}`;
    }

    grouped[matcher].push({
      id: hook.id,
      command,
      script: hook.script ? getScriptFilename(hook.script) : undefined,
      timeout: hook.timeout || 60000,
      failClosed: hook.failClosed || false,
      if: hook.if,
    });
  }

  return grouped;
}

function groupSessionHooksByEvent(hooks: HookDefinition[]): Record<string, HookEntry[]> {
  const grouped: Record<string, HookEntry[]> = {};

  for (const hook of hooks) {
    const event = (hook.event || "").toLowerCase().replace(/\s+/g, "");
    if (!event) continue;

    if (!grouped[event]) grouped[event] = [];

    // Synthesize command for enforcement hooks that don't have explicit command/script
    let command = hook.command;
    if (!command && !hook.script && ENFORCEMENT_HOOKS.has(hook.id)) {
      command = `loaf check --hook ${hook.id}`;
    }

    grouped[event].push({
      id: hook.id,
      command,
      script: hook.script ? getScriptFilename(hook.script) : undefined,
      timeout: hook.timeout || 60000,
      failClosed: hook.failClosed || false,
      if: hook.if,
    });
  }

  return grouped;
}

function getScriptFilename(scriptPath: string): string {
  const parts = scriptPath.split("/");
  return parts.slice(-2).join("/");
}

interface HookEntry {
  id: string;
  command?: string;
  script?: string;
  timeout: number;
  failClosed: boolean;
  if?: string;
}
