/**
 * Shared Runtime Plugin Generator
 *
 * Unified runtime plugin generator for TypeScript-plugin harnesses
 * (OpenCode + Amp). Generates platform-specific hooks.ts files.
 */

import { writeFileSync, mkdirSync } from "fs";
import { join } from "path";
import type { HooksConfig, HookDefinition } from "../../types.js";

const ENFORCEMENT_HOOKS = new Set([
  "check-secrets",
  "validate-push",
  "validate-commit",
  "workflow-pre-pr",
  "security-audit",
]);

// ─────────────────────────────────────────────────────────────────────────────
// Platform Configuration Interface
// ─────────────────────────────────────────────────────────────────────────────

export interface RuntimePlatform {
  platform: "opencode" | "amp";
  header: string;
  events: {
    preTool: string;
    postTool: string;
    sessionStart?: string;
    sessionEnd?: string;
    sessionCompacting?: string;
  };
  toolNameAccessor: string;
  toolInputAccessor: string;
  wrapExport: (body: string) => string;
  rejectPattern: (msgVar: string) => string;
  extraEvents?: (config: HooksConfig, srcDir: string) => string;
}

